#!/usr/bin/env python3
"""
Find globals that were incorrectly reused from the demo in the full game source.

Uses code/globals.h (demo) and code-full/globals.h (full game) as address
references (auto-generated by Ghidra), then scans src/*.cpp for demo-only
addresses used in function implementations.

Usage:
    python3 bin/findWrongGlobals.py            # concise output
    python3 bin/findWrongGlobals.py -v         # show every line reference
    python3 bin/findWrongGlobals.py -a         # also include globals.h/globals.cpp
    python3 bin/findWrongGlobals.py -f FILE    # only scan specific file(s)
"""

import re
import os
import sys
import glob
import argparse
from collections import defaultdict

DEMO_GLOBALS = "code/globals.h"
FULL_GLOBALS = "code-full/globals.h"
SRC_DIR = "src"

# Skip PE header and code section addresses
MIN_DATA_ADDR = 0x00430000

# Files that are declarations, not function implementations
DECL_FILES = {'globals.h', 'globals.cpp'}


def extract_addresses(filepath):
    """Extract {address: varname} from auto-generated globals.h."""
    addr_map = {}
    with open(filepath) as f:
        for line in f:
            if line.lstrip().startswith('//'):
                continue
            for m in re.finditer(r'\b([A-Za-z_]\w*?_([0-9A-Fa-f]{8}))\b', line):
                addr = m.group(2).lower()
                if int(addr, 16) >= MIN_DATA_ADDR:
                    addr_map[addr] = m.group(1)
    return addr_map


def scan_file(filepath, demo_only, full_addrs):
    """Returns list of (line, addr, varname, type)."""
    hits = []
    with open(filepath) as f:
        lines = f.readlines()

    for i, line in enumerate(lines):
        for m in re.finditer(r'\b([A-Za-z_]\w*?_([0-9A-Fa-f]{8}))\b', line):
            addr = m.group(2).lower()
            if int(addr, 16) < MIN_DATA_ADDR:
                continue
            if addr in demo_only:
                hits.append((i + 1, addr, m.group(1), 'demo'))
            elif addr not in full_addrs:
                hits.append((i + 1, addr, m.group(1), 'unknown'))
    return hits


def check_name_comment_mismatch(filepath):
    """Find lines where variable name address != comment address."""
    mismatches = []
    with open(filepath) as f:
        lines = f.readlines()
    for i, line in enumerate(lines):
        name_m = re.search(r'\b[A-Za-z_]\w*?_([0-9A-Fa-f]{8})\b', line)
        comment_m = re.search(r'//\s*(?:0x)?([0-9A-Fa-f]{8})\b', line)
        if name_m and comment_m:
            na = name_m.group(1).lower()
            ca = comment_m.group(1).lower()
            if na != ca and int(na, 16) >= MIN_DATA_ADDR:
                # Skip array element/range comments
                if any(kw in line for kw in ('Element', 'Within', 'indices', 'End of', 'offset')):
                    continue
                mismatches.append((i + 1, na, ca, line.rstrip()))
    return mismatches


def get_name_prefix(varname):
    """Strip hex suffix to get logical name: g_Buffer_00436960 -> g_Buffer"""
    prefix = re.sub(r'_[0-9A-Fa-f]{8}$', '', varname)
    # Ignore generic prefixes that match everything
    if prefix in ('DAT', 'PTR', 'PTR_LAB', 'PTR_FUN', 'FUN', 's'):
        return None
    return prefix


def main():
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument('-v', '--verbose', action='store_true',
                    help='Show every line reference')
    ap.add_argument('-a', '--all', action='store_true',
                    help='Include globals.h/globals.cpp (declarations)')
    ap.add_argument('-f', '--file', action='append', dest='files',
                    help='Only scan specific file(s)')
    args = ap.parse_args()

    demo_map = extract_addresses(DEMO_GLOBALS)
    full_map = extract_addresses(FULL_GLOBALS)
    demo_addrs = set(demo_map.keys())
    full_addrs = set(full_map.keys())
    demo_only = demo_addrs - full_addrs

    print(f"Demo: {len(demo_addrs)} addrs  Full: {len(full_addrs)} addrs  "
          f"Shared: {len(demo_addrs & full_addrs)}  Demo-only: {len(demo_only)}\n")

    # Determine files to scan
    if args.files:
        src_files = []
        for f in args.files:
            path = f if os.path.exists(f) else os.path.join(SRC_DIR, f)
            if os.path.exists(path):
                src_files.append(path)
            else:
                print(f"WARNING: {f} not found", file=sys.stderr)
    else:
        src_files = sorted(glob.glob(os.path.join(SRC_DIR, "*.cpp")) +
                           glob.glob(os.path.join(SRC_DIR, "*.h")))

    # Optionally exclude declaration files
    if not args.all and not args.files:
        src_files = [f for f in src_files if os.path.basename(f) not in DECL_FILES]

    # Scan all files
    file_hits = {}
    for filepath in src_files:
        hits = scan_file(filepath, demo_only, full_addrs)
        if hits:
            file_hits[os.path.basename(filepath)] = hits

    # --- Build per-address summary ---
    addr_info = defaultdict(lambda: {'vars': set(), 'files': defaultdict(int)})
    total_demo = 0

    for filename, hits in file_hits.items():
        for line, addr, varname, typ in hits:
            if typ == 'demo':
                addr_info[addr]['vars'].add(varname)
                addr_info[addr]['files'][filename] += 1
                total_demo += 1

    # --- DEMO-ONLY ADDRESSES ---
    if addr_info:
        print("=" * 80)
        print("DEMO-ONLY ADDRESSES in src/ function implementations")
        print("(exist in code/globals.h but NOT in code-full/globals.h)")
        print("=" * 80)

        if args.verbose:
            for filename, hits in sorted(file_hits.items()):
                demo_hits = [(l, a, v) for l, a, v, t in hits if t == 'demo']
                if demo_hits:
                    print(f"\n  {filename}:")
                    for line, addr, varname in demo_hits:
                        print(f"    L{line:4d}: {varname}  (0x{addr})")
        else:
            for filename in sorted(set(fn for a in addr_info.values() for fn in a['files'])):
                entries = []
                for addr, info in sorted(addr_info.items()):
                    if filename in info['files']:
                        cnt = info['files'][filename]
                        vname = sorted(info['vars'])[0]
                        entries.append((addr, vname, cnt))
                if entries:
                    print(f"\n  {filename}:")
                    for addr, vname, cnt in entries:
                        sfx = f" (x{cnt})" if cnt > 1 else ""
                        # Try to find a full-game equivalent
                        prefix = get_name_prefix(vname)
                        replacement = ""
                        if prefix:
                            for fa, fn in full_map.items():
                                fp = get_name_prefix(fn)
                                if fp and fp == prefix:
                                    replacement = f"  =>  {fn}"
                                    break
                        print(f"    0x{addr}  {vname}{sfx}{replacement}")

    else:
        print("No demo-only addresses found in src/ implementations.")

    # --- UNKNOWN ADDRESSES ---
    unknown_addrs = defaultdict(lambda: {'vars': set(), 'files': set()})
    total_unknown = 0
    for filename, hits in file_hits.items():
        for line, addr, varname, typ in hits:
            if typ == 'unknown':
                unknown_addrs[addr]['vars'].add(varname)
                unknown_addrs[addr]['files'].add(filename)
                total_unknown += 1

    if unknown_addrs:
        print(f"\n{'=' * 80}")
        print("UNKNOWN ADDRESSES (not in demo OR full game auto-generated globals)")
        print(f"{'=' * 80}")
        for addr in sorted(unknown_addrs.keys()):
            info = unknown_addrs[addr]
            vname = sorted(info['vars'])[0]
            files = sorted(info['files'])
            print(f"  0x{addr}  {vname}  [{', '.join(files)}]")

    # --- NAME/COMMENT MISMATCHES ---
    total_mismatch = 0
    header_shown = False
    for filepath in src_files:
        mm = check_name_comment_mismatch(filepath)
        if mm:
            if not header_shown:
                print(f"\n{'=' * 80}")
                print("NAME vs COMMENT ADDRESS MISMATCHES")
                print(f"{'=' * 80}")
                header_shown = True
            print(f"\n  {os.path.basename(filepath)}:")
            for line, na, ca, ctx in mm:
                print(f"    L{line:4d}: name=0x{na}  comment=0x{ca}")
                print(f"           {ctx.strip()}")
                total_mismatch += 1

    # --- SUMMARY ---
    print(f"\n{'=' * 80}")
    print(f"TOTAL: {total_demo} demo-addr refs in {len(addr_info)} unique addresses")
    print(f"       {total_unknown} unknown-addr refs in {len(unknown_addrs)} unique addresses")
    print(f"       {total_mismatch} name/comment mismatches")
    print(f"{'=' * 80}")

    if total_demo > 0:
        sys.exit(1)


if __name__ == '__main__':
    main()
